// Раскладки лабиринтов Арены — стены-линии в том же формате, что DUNGEON_LAYOUTS
// (client/src/data/dungeonLayouts.js): физика 60px/визуал 150px, координаты — offsets
// от центра мира, GameScene клипает их кругами клиренса (650px вокруг центра —
// у арены нет гейтов, так что это единственная авто-прорезь).
//
// Абсолютные позиции баз/точек/спавна груза ДОЛЖНЫ совпадать с server/main.py
// ARENA_SPAWNS/ARENA_POINT_POSITIONS/ARENA_CARGO_SPAWN (там те же offsets + центр
// мира по той же формуле BASE_WORLD×scale/2).
//
// Каждый режим несёт 3 варианта лабиринта (variants[].walls) — выбор НА МАТЧ, не по
// дню: сервер кидает кубик при создании матча (см. server/arena.py ArenaMatch.
// maze_variant, ARENA_MAZE_VARIANTS) и шлёт индекс в arena_match_found, оба игрока
// матча получают ОДИН и тот же индекс. См. диалог: "все берём, выбор — рандом".
//
// Варианты 2/3 — ТОТ ЖЕ generator+сид, что показан в артефакте выбора (10×6 сетка,
// recursive backtracker, mulberry32(1156)/mulberry32(722)+braid) — раньше здесь была
// топология со своими сеткой/сидами "в том же стиле", но не идентичная показанному и
// одобренному варианту (баг из диалога: "нарисовано в артефакте одно, а реализовано
// другое — сделай один в один"). Плюс: после генерации КАЖДЫЙ вариант обрезан кругом
// клиренса (radius ~260, чуть больше ARENA_BASE_SAFE_R) вокруг позиции базы — тем же
// алгоритмом клипа, что GameScene.createDungeonWalls() уже использует для центра
// мира/гейтов — раньше этого клипа не было У БАЗЫ (только у центра), и сетка иногда
// клала стену прямо через безопасную зону базы (баг из диалога: "стена на базе аж до
// самого центра"). Результат запечён в статические координаты один раз при
// авторинге (тот же принцип, что и у DUNGEON_LAYOUTS) — генератор не едет в бандл.
const mirrorX = walls => walls.flatMap(([x1, y1, x2, y2]) => [[x1, y1, x2, y2], [-x1, y1, -x2, y2]]);
const rot180 = walls => walls.flatMap(([x1, y1, x2, y2]) => [[x1, y1, x2, y2], [-x1, -y1, -x2, -y2]]);

