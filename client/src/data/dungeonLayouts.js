// как на босс-карте R-1-boss) + 3 суточных варианта размещения контента.
// R-1-boss сюда НЕ входит — его геометрия остаётся в createDungeonWalls.
//
// Координаты — offsets от центра мира (x вправо, y вниз).
// walls: [x1,y1,x2,y2] осевые линии; GameScene клипает их кругами клиренса
// (650px вокруг центра и гейтов), так что проёмы у входа образуются автоматически.
// bossDoor: [x,y,w,h] — прямоугольник двери, рёбра перекрывают соседние стены ≥50px
// (иначе останется LOS-«щель» в углу).
//
// variants (выбор по датному сиду, см. GameScene._dungeonVariantIndex):
//   spots    — обычные мобы пула (ключ по i % pool.length), кол-во фиксировано
//              (DUNGEON_MOB_GROWTH в constants.js посчитан от него)
//   guards   — элитные охранники [key,x,y] (кол-во и ключи = старым картам,
//              иначе раздуется канал 100% дропа элиток)
//   boss     — [x,y] главбосса; escorts — [key,x,y] охрана босса (кол-во = старому)
//   deposits — точки ресурсных россыпей (кол-во = старому: D1:3 D2:5 D3:3 D4:3 D5:4 prem:3)
// reinforceZones — зоны подкреплений hard/elite (общие для вариантов)

// Горизонтальная линия y, от x1 до x2, с вырезами [gx1,gx2]
const hline = (y, x1, x2, ...gaps) => {
  const segs = [];
  let cur = x1;
  for (const [g1, g2] of [...gaps].sort((a, b) => a[0] - b[0])) {
    if (g1 > cur) segs.push([cur, y, g1, y]);
    cur = Math.max(cur, g2);
  }
  if (cur < x2) segs.push([cur, y, x2, y]);
  return segs;
};
// Вертикальная линия x, от y1 до y2, с вырезами [gy1,gy2]
const vline = (x, y1, y2, ...gaps) => {
  const segs = [];
  let cur = y1;
  for (const [g1, g2] of [...gaps].sort((a, b) => a[0] - b[0])) {
    if (g1 > cur) segs.push([x, cur, x, g1]);
    cur = Math.max(cur, g2);
  }
  if (cur < y2) segs.push([x, cur, x, y2]);
  return segs;
};

